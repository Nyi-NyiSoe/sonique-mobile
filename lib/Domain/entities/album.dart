

class Album {
  int? artistId;
  String? coverImageUrl;
  String? created_at;
  String? description;
  int? id;
  String? name;
  String? updated_at;

  Album({
    required this.artistId,
    required this.coverImageUrl,
    required this.created_at,
    required this.description,
    required this.id,
    required this.name,
    required this.updated_at,
  });
}
